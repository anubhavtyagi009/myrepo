package com.bec.orrreporting.config;

import com.fasterxml.jackson.databind.SerializationFeature;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.http.converter.json.MappingJackson2HttpMessageConverter;
import org.springframework.stereotype.Service;
import org.springframework.web.client.RestTemplate;

@Configuration
@Service
public class RestTemplateConfig {

    private final Logger log = LoggerFactory.getLogger(RestTemplateConfig.class);

    @Autowired
    BenchmarkProperties benchmarkProperties;

    public RestTemplateConfig(BenchmarkProperties benchmarkProperties) {
        this.benchmarkProperties = benchmarkProperties;
    }

    @Bean
    public RestTemplate restTemplate() {

        log.debug("Registering RestTemplate");

        RestTemplate restTemplate = new RestTemplate();

        MappingJackson2HttpMessageConverter jsonHttpMessageConverter = new MappingJackson2HttpMessageConverter();
        jsonHttpMessageConverter.getObjectMapper().configure(SerializationFeature.FAIL_ON_EMPTY_BEANS, false);

        restTemplate.getMessageConverters().add(jsonHttpMessageConverter);

        return restTemplate;
    }
}
