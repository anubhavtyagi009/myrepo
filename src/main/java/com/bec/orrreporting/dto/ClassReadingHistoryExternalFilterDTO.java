package com.bec.orrreporting.dto;

public class ClassReadingHistoryExternalFilterDTO {

    private Integer classId;
    private String startDate;
    private String endDate;

    public ClassReadingHistoryExternalFilterDTO(Integer classId, String startDate, String endDate) {
        this.classId = classId;
        this.startDate = startDate;
        this.endDate = endDate;
    }

    public ClassReadingHistoryExternalFilterDTO() {
    }

    public Integer getClassId() {
        return classId;
    }

    public void setClassId(Integer classId) {
        this.classId = classId;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }
}
